# if / elif / else
# se / se não se / se não
entrada = input('Voce quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print('Voce entrou no sistema')
elif entrada == 'sair':
    print('Voce saiu no sistema')
elif entrada == 'segredo':
    print('Voce encontrou o segredo :D')
else:
    print('Entrada invalida ;-;')