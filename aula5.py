# Tipo de dado bool(boolean)
# Ao questionar algo em um programa
# só existe duas respostas possiveis:
# sim (True) ou não (False).
# Existem varios operadores de 'questionar'.
# Dentro deles o ==, que é um operador logico
# que questiona se um valor é igual a outro
print(10 == 10)
print(10 == 9)
print(type(10 == 9))
print(type(10 == 10))
